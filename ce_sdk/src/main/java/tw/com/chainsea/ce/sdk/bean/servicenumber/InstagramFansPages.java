package tw.com.chainsea.ce.sdk.bean.servicenumber;

public class InstagramFansPages {
    private String code;
    private String instagramName;
    private String name;
    private String groupCode;
    private String fansPageAccessToken;
    private String instagramBotName;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getInstagramName() {
        return instagramName;
    }

    public void setInstagramName(String instagramName) {
        this.instagramName = instagramName;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGroupCode() {
        return groupCode;
    }

    public void setGroupCode(String groupCode) {
        this.groupCode = groupCode;
    }

    public String getFansPageAccessToken() {
        return fansPageAccessToken;
    }

    public void setFansPageAccessToken(String fansPageAccessToken) {
        this.fansPageAccessToken = fansPageAccessToken;
    }

    public String getInstagramBotName() {
        return instagramBotName;
    }

    public void setInstagramBotName(String instagramBotName) {
        this.instagramBotName = instagramBotName;
    }
}


